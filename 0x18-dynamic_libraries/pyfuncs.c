#include "pyfuncs.h"
/**
 * add - add 
 * @a: a
 * @b: b
 * Return: result of add
 */
int add(a, b)
{
	return (a + b);
}
/**
 * sub - sub
 * @a: a
 * @b: b
 * Return: result of sub
 */
int sub(a, b)
{
	return (a - b);
}
/**
 * mul - multiply
 * @a: a
 * @b: b
 * Return: a * b
 */
int mul(a, b)
{
	int c = a * b;
	return (c);
}
/**
 * div - divide
 * @a: a
 * @b: b
 * Return: result of div
 */
int div(a, b)
{
	return (a / b);
}
/**
 * mod - modulus
 * @a: a
 * @b: b
 * Return: result of mod
 */
int mod(a, b)
{
	return (a % b);
}
